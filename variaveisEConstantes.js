var a = 3 // var é uma palavra reservada, para definir uma variavel, a é o nome da variavel (os nomes são importantes para definir o que a variavel é), e 3 é o valor 
let b = 4 //let é uma palavra reservada para declarar variaveis, uma forma mais moderna (usar no lugar do var)
/*
Com o var você pode redeclarar uma mesma variável
Já com o let isto não é possível 
Para uma maior segurança usar sempre let
*/
const c = 5 //Const é uma palavra reservada para definir uma constante, este tipo não pode ser alterado o falor depois.
/*
Usar constante sempre que algo não tem motivo para mudar durante o programa
*/